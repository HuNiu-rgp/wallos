export function useI18n() {
    const t = (key) => messages[currentLocale.value]?.[key] || messages.en[key] || key;
    const setLocale = (locale) => {
        currentLocale.value = locale;
        window.localStorage.setItem('wallos.locale', locale);
    };

    return {
        currentLocale: computed(() => currentLocale.value),
        setLocale,
        t,
    };
}

export function money(cents, currency = 'USD') {
    const symbol = {
        AUD: '$',
        CAD: '$',
        CNY: '￥',
        EUR: '€',
        GBP: '£',
        HKD: 'HK$',
        JPY: '￥',
        SGD: '$',
        TWD: 'NT$',
        USD: '$',
    }[currency] || '';

    return `${symbol}${new Intl.NumberFormat(currentLocale.value === 'zh' ? 'zh-CN' : 'en-US', {
        minimumFractionDigits: 2,
        maximumFractionDigits: 2,
    }).format((cents || 0) / 100)} ${currency}`;
}

export function centsToAmount(cents) {
    return ((cents || 0) / 100).toFixed(2);
}
